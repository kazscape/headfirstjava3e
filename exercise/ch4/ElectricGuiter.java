class ElectricGuiter {
  String brand;
  int numOfPickups;
  boolean rockStarUseIt;

  String getBrand() {
    return brand;
  }

  void setBrand(String aBrand) {
    brand = aBrand;
  }

  int getNumOfPickups() {
    return numOfPickups;
  }

  void setNumOfPickups(int num) {
    numOfPickups = num;
  }

  boolean getRockSterUseIt() {
    return rockSterUseIt;
  }

  void setRockStarUseIt(boolean yesOrNo) {
    rockSterUseIt = yesOrNo;
  }
}
